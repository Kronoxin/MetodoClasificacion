package metodosclasificacion.lloyd;

/**
 *
 * @author Ruben
 */
public class Nodo 
{
    private String nombre;
    private double x;
    private double y;
    
    public Nodo(String nombre, double x, double y)
    {
        this.nombre = nombre;
        this.x = x;
        this.y = y;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
    
}
